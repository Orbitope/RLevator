        // ── Layout resolution ─────────────────────────────────────────────────

        void ResolveLayout()
        {
            // Reference resolution 1280x800, aside=336, header=50, footer=64
            _sectionW    = 1280f - AsideW - SectionPadX * 2f;
            float mainH  = 800f - HeaderH - FooterH - SectionPadTop * 2f;
            _fh          = Mathf.Clamp(Mathf.Floor(mainH / _cfg.numFloors), MinFH, MaxFH);
            _tileH       = Mathf.Clamp(Mathf.Floor((_fh - 16f) / 2f) - 1f, MinTileH, MaxTileH);
            _shaftsWidth = _cfg.numElevators * ShaftW + (_cfg.numElevators - 1) * ShaftGap;
            _rightPad    = _shaftsWidth + 30f;
        }

        // ── UI BUILD ──────────────────────────────────────────────────────────

        void BuildUI()
        {
            ResolveLayout();

            // Use THIS GameObject's RectTransform as the actual build root
            var rt = GetComponent<RectTransform>();
            if (rt == null) rt = gameObject.AddComponent<RectTransform>();
            var buildRoot = rt;

            // Clear any previous UI children (only children of THIS rect, not siblings)
            for (int i = buildRoot.childCount - 1; i >= 0; i--)
                Destroy(buildRoot.GetChild(i).gameObject);

            // Root bg
            var rootImg = buildRoot.gameObject.GetComponent<Image>();
            if (rootImg == null) rootImg = buildRoot.gameObject.AddComponent<Image>();
            rootImg.color = Void;
            rootImg.raycastTarget = false;

            // reassign uiRoot to this object so sub-methods use the right parent
            uiRoot = buildRoot;

            BuildHeader(uiRoot);
            BuildFooter(uiRoot);
            BuildMain(uiRoot);

            RefreshView();
            UpdateFooterState();
        }

