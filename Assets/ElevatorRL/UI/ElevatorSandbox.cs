            });
            _floorsDropdown.onValueChanged.AddListener(i =>
            {
                startFloors = i + 3;
                _cfg.numFloors = startFloors;
                BuildSim();
                RebuildDynamicUI();
            });
            _carsDropdown.onValueChanged.AddListener(i =>
            {
                startCars = i + 1;
                _cfg.numElevators = startCars;
                BuildSim();
                RebuildDynamicUI();
            });
            _capDropdown.onValueChanged.AddListener(i =>
            {
                startCapacity = new[] { 4, 6, 8, 10, 12, 16 }[i];
