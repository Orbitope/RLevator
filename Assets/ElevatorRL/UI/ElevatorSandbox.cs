        // ── Simulation ────────────────────────────────────────────────────────

        void BuildSim()
        {
            _cfg     = buildingConfigAsset    ? Instantiate(buildingConfigAsset)    : ScriptableObject.CreateInstance<BuildingConfig>();
            _reward  = rewardConfigAsset      ? Instantiate(rewardConfigAsset)      : ScriptableObject.CreateInstance<RewardConfig>();
            _obs     = observationConfigAsset ? Instantiate(observationConfigAsset) : ScriptableObject.CreateInstance<ObservationConfig>();
            _traffic = trafficConfigAsset     ? Instantiate(trafficConfigAsset)     : ScriptableObject.CreateInstance<TrafficConfig>();

            _cfg.numFloors    = startFloors;
            _cfg.numElevators = startCars;
            _cfg.capacity     = startCapacity;
            _cfg.randomizeActive          = false;
            _cfg.serviceChangeProbability = 0f;
            _cfg.minActiveElevators       = 1;
            _traffic.useDayCycle     = false;
            _traffic.defaultPattern  = startPattern;
            _traffic.intensity       = intensity;
            _selectedPattern         = startPattern;

            _b            = new Building(_cfg, _reward, _obs, _traffic, seed);
            _b.Reset();
            _decisions    = 0;
            _total        = 0;
            _last         = 0;
            _utilSum      = new float[_cfg.numElevators];
            _utilSteps    = 0;
            _decisionClock= 0;
        }

        void Advance(float simDelta)
        {
            const float MaxSub = 0.05f;
            while (simDelta > 1e-6f)
            {
                float h = Mathf.Min(MaxSub, simDelta);
                _b.Tick(h);
                simDelta -= h;
                _decisionClock += h;
                if (_decisionClock >= _cfg.decisionInterval)
                {
                    _decisionClock -= _cfg.decisionInterval;
                    Decide();
                }
            }
        }

        void Decide()
        {
            _traffic.defaultPattern = ActivePattern();
            var act = ElevatorHeuristics.CollectiveLook(_b);
            for (int i = 0; i < _cfg.numElevators; i++)
                _b.ApplyAction(i, act[i]);
            _last  = _b.CollectReward();
            _total += _last;
            for (int i = 0; i < _cfg.numElevators; i++)
                _utilSum[i] += _b.cars[i].Load;
            _utilSteps++;
            _decisions++;
        }

        void StepOnce()
        {
            if (_playing) return;
            int  d     = _decisions;
            int  guard = 0;
            while (_decisions == d && guard++ < 100000)
                Advance(0.05f);
            RefreshView();
        }

        const int MinutesPerStep = 2;

        TrafficPattern ActivePattern()
        {
            if (!_dayCycle) return _selectedPattern;
            int   m = (6 * 60 + _decisions * MinutesPerStep) % 1440;
            float h = m / 60f;
            if (h >= 6  && h < 10) return TrafficPattern.UpPeak;
            if (h >= 11 && h < 14) return TrafficPattern.Lunch;
            if (h >= 16 && h < 19) return TrafficPattern.DownPeak;
            return TrafficPattern.Midday;
        }

        string Clock()
        {
            int m = (6 * 60 + _decisions * MinutesPerStep) % 1440;
            return $"{m / 60:00}:{m % 60:00}";
        }

