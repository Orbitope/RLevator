            _floorsDropdown.SetValueWithoutNotify(startFloors - 3);
            _carsDropdown.SetValueWithoutNotify(startCars - 1);
            _capDropdown.SetValueWithoutNotify(CapIndex(startCapacity));
            _patternDropdown.SetValueWithoutNotify((int)startPattern);

