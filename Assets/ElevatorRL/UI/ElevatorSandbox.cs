        // ── RefreshView ───────────────────────────────────────────────────────

        void RefreshView()
        {
            if (_b == null) return;

            // Header
            _headerClock.text   = Clock();
            _headerPattern.text = PatternLabel(ActivePattern());
            _headerStep.text    = _decisions.ToString();

            // Reward
            _rewardTotal.text = $"{_total:F1}";
            _rewardDelta.color = _last >= 0 ? AmberBright : SteelBright;
            string sign = _last >= 0 ? "+" : "";
            _rewardDelta.text = $"{sign}{_last:F2} / step";

            // KPIs
            float thruPerStep = _decisions > 0 ? (float)(_b.DeliveredTotal / (double)_decisions) : 0f;
            float avgWait     = _b.WaitCount > 0 ? _b.WaitSum / _b.WaitCount : 0f;
            _kpiValues[0].text = _b.DeliveredTotal.ToString();
            _kpiValues[1].text = $"{thruPerStep:F2}";
            _kpiValues[2].text = $"{avgWait:F1}";
            _kpiValues[3].text = Mathf.RoundToInt(_b.MaxWaitObserved).ToString();
            _kpiValues[4].text = _b.RejectedTotal.ToString();
            _kpiValues[5].text = _b.AbandonedTotal.ToString();

            // Utilization
            for (int i = 0; i < _cfg.numElevators && i < _utilFillRt.Length; i++)
            {
                float avg = _utilSteps > 0 ? _utilSum[i] / _utilSteps : 0f;
                float fillW = avg * _utilTrackW;
                _utilFillRt[i].offsetMax = new Vector2(fillW, 0);
                _utilPct[i].text = $"{avg * 100f:F0}%";
            }

            // Floor bands: call arrows + tiles
            int NF = _cfg.numFloors;
            for (int f = 0; f < NF && f < _floors.Length; f++)
            {
                var fu = _floors[f];
                bool upActive   = _b.upQ[f].Count > 0;
                bool downActive = _b.downQ[f].Count > 0;

                fu.upCallImg.color   = upActive
                    ? new Color(AmberBright.r, AmberBright.g, AmberBright.b, 1f)
                    : new Color(TextMuted.r, TextMuted.g, TextMuted.b, 0.3f);
                fu.downCallImg.color = downActive
                    ? new Color(AmberBright.r, AmberBright.g, AmberBright.b, 1f)
                    : new Color(TextMuted.r, TextMuted.g, TextMuted.b, 0.3f);

                RefreshTileRow(fu.upTrack,   _b.upQ[f],   true);
                RefreshTileRow(fu.downTrack, _b.downQ[f], false);
            }

            // Queue bars
            for (int f = NF - 1; f >= 0; f--)
            {
                int fi = NF - 1 - f;
                if (fi >= _qrows.Length) continue;
                var qr = _qrows[fi];
                int upCount = _b.upQ[f].Count;
                int dnCount = _b.downQ[f].Count;
                float upFrac = Mathf.Clamp01(upCount / (float)_cfg.maxQueue);
                float dnFrac = Mathf.Clamp01(dnCount / (float)_cfg.maxQueue);
                qr.upBar.offsetMax = new Vector2(upFrac * _queueTrackW, 0);
                qr.dnBar.offsetMax = new Vector2(dnFrac * _queueTrackW, 0);
                qr.counts.text = $"{upCount}/{dnCount}";
            }

            // Elevator cars
            for (int i = 0; i < _cfg.numElevators && i < _cars.Length; i++)
            {
                var car = _b.cars[i];
                var cui = _cars[i];

                bool doorOpen = car.state == CarState.DoorsOpening
                             || car.state == CarState.Dwelling
                             || car.state == CarState.DoorsClosing;

                // car position (top from top of shaft)
                float carTopFromShaftTop = (NF - 1 - car.position) * _fh + 4f;
                cui.carRect.anchoredPosition = new Vector2(3, -carTopFromShaftTop);

                cui.doorStrip.gameObject.SetActive(doorOpen);

                // border color
                Color borderCol = doorOpen ? AmberBright : Amber;
                cui.border.color = borderCol;

                // load label
                int riders  = car.riders.Count;
                int cap     = _cfg.capacity;
                cui.loadLabel.text = $"{riders}/{cap}";

                // dots
                int dotCount = Mathf.Min(riders, 18);
                var dotsT    = cui.dotsParent;
                // clear old dots immediately so layout sees only new children
                for (int d = dotsT.childCount - 1; d >= 0; d--)
                    DestroyImmediate(dotsT.GetChild(d).gameObject);
                for (int d = 0; d < dotCount; d++)
                {
                    var dot = CKUI.Box(dotsT, "Dot", Amber);
                    dot.sprite = _circleSprite;
                    var drt = dot.rectTransform;
                    var dle = dot.gameObject.AddComponent<LayoutElement>();
                    dle.minWidth = dle.minHeight = 5;
                    dle.flexibleWidth = 0;
                }

                // direction indicator
                Color dirColor = car.dir > 0 ? Amber : SteelBright;
                cui.dirTriangle.color = dirColor;
            }

            // Observation
            if (showObservation && _obsLocations != null)
            {
                _sb.Clear();
                _sb.Append("car_locations: [");
                for (int i = 0; i < _cfg.numElevators; i++)
                {
                    if (i > 0) _sb.Append("  ");
                    _sb.Append(_b.cars[i].Floor);
                }
                _sb.Append("]");
                _obsLocations.text = _sb.ToString();

                _sb.Clear();
                for (int i = 0; i < _cfg.numElevators; i++)
                {
                    _sb.Append($"E{i} ");
                    for (int f = 0; f < NF; f++)
                        _sb.Append(_b.cars[i].WantsFloor(f) ? '1' : '0');
                    if (i < _cfg.numElevators - 1) _sb.Append('\n');
                }
                _obsButtons.text = _sb.ToString();
                _obsButtons.color = (_cfg.numElevators > 0 && _b.cars[0].riders.Count > 0)
                    ? Amber : TextMuted;

                _sb.Clear();
                _sb.Append("hall [up dn]: ");
                for (int f = 0; f < NF; f++)
                {
                    _sb.Append(_b.upQ[f].Count > 0 ? '1' : '0');
                    _sb.Append(_b.downQ[f].Count > 0 ? '1' : '0');
                    if (f < NF - 1) _sb.Append(' ');
                }
                _obsHall.text = _sb.ToString();
            }
        }

        void RefreshTileRow(Transform track, List<Passenger> queue, bool up)
        {
            // Clear old tiles immediately so layout sees only the new children this frame
            for (int c = track.childCount - 1; c >= 0; c--)
                DestroyImmediate(track.GetChild(c).gameObject);

            int show = Mathf.Min(queue.Count, MaxTiles);
            for (int t = 0; t < show; t++)
            {
                var p       = queue[t];
                bool urgent = p.waitTime >= 0.8f * _cfg.maxWait;

                var tile = CKUI.Panel(track, $"Tile{t}", Raised);
                tile.sizeDelta = new Vector2(18, _tileH);
                AddBorder1px(tile, urgent ? Coral : Border);
                tile.gameObject.AddComponent<LayoutElement>().flexibleWidth = 0;

                string destTxt = p.dest == 0 ? "G" : p.dest.ToString();
                var lbl = CKUI.Label(tile, "Dest", destTxt, monoFont, 9,
                    urgent ? CoralBright : TextSecondary, TextAlignmentOptions.Midline);
                CKUI.Stretch(lbl.rectTransform, 0, 0, 2, 4);

                // wait fill bar at bottom
                float waitFrac = Mathf.Clamp01(p.waitTime / _cfg.maxWait);
                var bar = CKUI.Box(tile, "WaitBar", urgent ? Coral : Amber);
                var barRt = bar.rectTransform;
                barRt.anchorMin = Vector2.zero;
                barRt.anchorMax = new Vector2(0, 0);
                barRt.pivot = new Vector2(0, 0);
                barRt.anchoredPosition = Vector2.zero;
                barRt.sizeDelta = new Vector2(waitFrac * 18f, 2);
            }

            if (queue.Count > MaxTiles)
            {
                int more = queue.Count - MaxTiles;
                var more_lbl = CKUI.Label(track, "More", $"+{more}", monoFont, 9, TextMuted,
                    TextAlignmentOptions.MidlineLeft);
                more_lbl.gameObject.AddComponent<LayoutElement>().flexibleWidth = 0;
            }
        }

        // ── Footer state ──────────────────────────────────────────────────────

        void UpdateFooterState()
        {
            if (_playBtnLabel == null) return;

            if (_playing)
            {
                _playBtnLabel.text = "PAUSE";
                var img = _playBtn.GetComponent<Image>();
                img.color = Raised;
                _playBtnLabel.color = AmberBright;
                AddBorder1px(_playBtn.GetComponent<RectTransform>(), Amber);
            }
            else
            {
                _playBtnLabel.text = "PLAY";
                var img = _playBtn.GetComponent<Image>();
                img.color = Amber;
                _playBtnLabel.color = Void;
                RemoveBorders(_playBtn.GetComponent<RectTransform>());
            }

            _stepBtn.interactable = !_playing;
            var stepImg = _stepBtn.GetComponentInChildren<TextMeshProUGUI>();
            if (stepImg) stepImg.color = _playing
                ? new Color(TextPrimary.r, TextPrimary.g, TextPrimary.b, 0.4f)
                : TextPrimary;

            if (_dayCycleBtnLabel != null)
                _dayCycleBtnLabel.text = $"DAY CYCLE  {(_dayCycle ? "ON" : "OFF")}";
        }

        // ── Utility ──────────────────────────────────────────────────────────

        static void AddBorder1px(RectTransform rt, Color c)
        {
            RemoveBorders(rt);
            CKUI.BorderTop(rt, c);
            CKUI.BorderBottom(rt, c);
            CKUI.BorderLeft(rt, c);
            CKUI.BorderRight(rt, c);
        }

        static void RemoveBorders(RectTransform rt)
        {
            for (int i = rt.childCount - 1; i >= 0; i--)
            {
                var child = rt.GetChild(i);
                string n = child.name;
                if (n == "BorderTop" || n == "BorderBot" || n == "BorderLeft" || n == "BorderRight")
                    Destroy(child.gameObject);
            }
        }

        static string[] FloorOptions()
        {
            var opts = new string[14];
            for (int i = 0; i < 14; i++) opts[i] = (i + 3).ToString();
            return opts;
        }

        static int CapIndex(int cap)
        {
            var caps = new[] { 4, 6, 8, 10, 12, 16 };
            for (int i = 0; i < caps.Length; i++)
                if (caps[i] == cap) return i;
            return 2; // default 8
        }
